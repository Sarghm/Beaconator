import UIKit

class UIColor_Beaconator: UIColor {
   
    class func lightGray() -> UIColor {
        return UIColor.whiteColor()
    }
    
    class func pink() -> UIColor {
        return UIColor.purpleColor()
    }
    
    class func green() -> UIColor {
        return UIColor.greenColor()
    }
    
}
